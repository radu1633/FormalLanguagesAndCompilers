public class Productie {
    private String simbol;
    private String cuvant;

    public Productie(String simbol, String cuvant){
        this.simbol = simbol;
        this.cuvant = cuvant;
    }

    public String getSimbol(){
        return simbol;
    }

    public String getCuvant(){
        return cuvant;
    }
}
